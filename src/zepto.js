  // `$.zepto.init` is Zepto's counterpart to jQuery's `$.fn.init` and
  // takes a CSS selector and an optional context (and handles various
  // special cases).
  // This method can be overridden in plugins.
  /** 
    `$.zepto.init` 与JQuery中的 `$.fn.init`类似,接收一个css选择器参数与一个可选的上下文参数
  */
  zepto.init = function(selector, context) {
    var dom
    // If nothing given, return an empty Zepto collection
    //如果没有选择器,则返回一个空的Zepto
    if (!selector) return zepto.Z()
    // Optimize for string selectors
    //给定的selector是字符串
    else if (typeof selector == 'string') {
      selector = selector.trim()
      // If it's a html fragment, create nodes from it
      // Note: In both Chrome 21 and Firefox 15, DOM error 12
      // is thrown if the fragment doesn't begin with <
      // 给定的字符串类似于 `<p></p>` ,则创建一个html节点
      if (selector[0] == '<' && fragmentRE.test(selector))
        dom = zepto.fragment(selector, RegExp.$1, context), selector = null
      // If there's a context, create a collection on that context first, and select
      // nodes from there
      // 若存在上下文参数,则创建上下文集合,并从中选择这个节点
      else if (context !== undefined) return $(context).find(selector)
      // If it's a CSS selector, use it to select nodes.
      //-- 扩展了 querySelectorAll 查询出来是dom数组
      else dom = zepto.qsa(document, selector)
    }
    // If a function is given, call it when the DOM is ready
    // 若为函数,则等Dom ready时执行
    else if (isFunction(selector)) return $(document).ready(selector)
    // If a Zepto collection is given, just return it
    //若为zepto对象,则直接返回自身
    else if (zepto.isZ(selector)) return selector
    else {
      // normalize array if an array of nodes is given
      //若给定的是集合,则将此数组过滤,筛选出item为非空的数组集合
      if (isArray(selector)) dom = compact(selector)
      // Wrap DOM nodes.
      //若为对象,则返回包含对象的数组
      else if (isObject(selector))
        dom = [selector], selector = null
      // If it's a html fragment, create nodes from it
      //若所传为html节点,则创建创建该节点
      else if (fragmentRE.test(selector))
        dom = zepto.fragment(selector.trim(), RegExp.$1, context), selector = null
      // If there's a context, create a collection on that context first, and select
      // nodes from there
      //若传进来上下文,则创建一个context,从中查找selector
      else if (context !== undefined) return $(context).find(selector)
      // And last but no least, if it's a CSS selector, use it to select nodes.
      else dom = zepto.qsa(document, selector)
    }
    // create a new Zepto collection from the nodes found
    //返回一个新的Zepto对象
    return zepto.Z(dom, selector)
  }

  // `$` will be the base `Zepto` object. When calling this
  // function just call `$.zepto.init, which makes the implementation
