  /** 对目标对象扩展原对象的属性 
    1.遍历source中的key值
    2.判断是否为深拷贝,然后判断source中key值对应的value是否为对象或者 数组
    3.若source中key值为对象或者数组,则判断原对象是否为对象,且目标key为非对象,
    4.3成立则将target[key] = {},设置为空对象,递归执行extend
    5.若原对象为数组且目标对象不是数组,则将目标对象设置为空数组,递归执行extend
    6. 若为浅拷贝则直接赋值
  */
  function extend(target, source, deep) {
    for (key in source)
      if (deep && (isPlainObject(source[key]) || isArray(source[key]))) {
        if (isPlainObject(source[key]) && !isPlainObject(target[key]))
          target[key] = {}
        if (isArray(source[key]) && !isArray(target[key]))
          target[key] = []
        extend(target[key], source[key], deep)
      }
      else if (source[key] !== undefined) target[key] = source[key]
  }

  // Copy all but undefined properties from one or more
  // objects to the `target` object.
  /** 
    * 坑爹的js参数
    参考用法得知 
    默认为浅拷贝,若第一个参数为true,则为深拷贝(真坑爹呀)
  */
  $.extend = function(target){
    var deep, args = slice.call(arguments, 1)
    if (typeof target == 'boolean') {
      deep = target
      target = args.shift()
    }
    args.forEach(function(arg){ extend(target, arg, deep) })
    return target
  }

  // `$.zepto.qsa` is Zepto's CSS selector implementation which
  // uses `document.querySelectorAll` and optimizes for some special cases, like `#id`.
  // This method can be overridden in plugins.
  // zepto 的querySelectorAll选择器,扩展了可以查询ID的方法
  zepto.qsa = function(element, selector){
    var found,
        maybeID = selector[0] == '#',
        maybeClass = !maybeID && selector[0] == '.',
        //把传入的第一个字符筛掉,如果是类选择器或者是id选择器
        nameOnly = maybeID || maybeClass ? selector.slice(1) : selector, // Ensure that a 1 char tag name still gets checked,
        //判断是否为简单选择器,例如 类选择器、id选择器或者tag选择器
        isSimple = simpleSelectorRE.test(nameOnly)
    return (element.getElementById && isSimple && maybeID) ? // Safari DocumentFragment doesn't have getElementById
      //若为id选择器,则根据传入值获取到具体Dom节点,并将其放入数组中,若未查询到,返回空数组
      ( (found = element.getElementById(nameOnly)) ? [found] : [] ) :
      //1是元素(Element) 9 是文档(Document) 11 是代表轻量级的 Document 对象，能够容纳文档的某个部分(DocumentFragment)
      (element.nodeType !== 1 && element.nodeType !== 9 && element.nodeType !== 11) ? [] :
      slice.call(
        isSimple && !maybeID && element.getElementsByClassName ? // DocumentFragment doesn't have getElementsByClassName/TagName
          //简单选择器而且是类选择器
          maybeClass ? element.getElementsByClassName(nameOnly) : // If it's simple, it could be a class
          //tag选择器,标签选择器
          element.getElementsByTagName(selector) : // Or a tag
          //都不是则需要查询所有
          element.querySelectorAll(selector) // Or it's not simple, and we need to query all
      )
  }

